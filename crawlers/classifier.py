"""상품명+브랜드 키워드 기반 카테고리 자동 분류.

리스트 순서 = 우선순위(위일수록 먼저 매칭). 상품명+브랜드를 소문자로 합쳐
키워드를 순서대로 검사해 첫 매칭 카테고리 1개만 배정한다. 미매칭 시 "기타/특수목적".
카테고리 추가/수정은 CATEGORY_MAP에 항목만 추가하면 된다.
"""

CATEGORY_MAP = [
    ("멀티비타민/종합비타민", ["멀티비타민", "종합비타민", "이뮨비타민"]),
    ("비타민C/항산화", ["비타민c", "c1000", "항산화", "아스코르브"]),
    ("비타민D", ["비타민d", "비타민디"]),
    ("마그네슘", ["마그네슘"]),
    ("루테인/눈건강", ["루테인", "제아잔틴", "눈건강", "오메가3"]),
    ("혈행", ["코엔자임", "q10", "코큐텐", "혈행", "혈액순환"]),
    ("홍삼/인삼", ["홍삼", "인삼", "고려홍삼", "정관장"]),
    ("다이어트/체중관리", ["다이어트", "슬리밍", "닭가슴살", "단백질", "크레아틴", "혈당", "바나바"]),
    ("프로바이오틱스/유산균", ["프로바이오틱스", "유산균", "락토바실러스", "비피더스"]),
    ("피부/미용", ["콜라겐", "피부", "히알루론산", "비오틴", "이너뷰티", "더마"]),
]

FALLBACK_CATEGORY = "기타/특수목적"


def classify(name: str, brand: str = "") -> str:
    text = f"{name} {brand}".lower()
    for category, keywords in CATEGORY_MAP:
        if any(keyword in text for keyword in keywords):
            return category
    return FALLBACK_CATEGORY
